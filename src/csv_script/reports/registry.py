from collections.abc import Callable

"""
Реестр (registry) отчётов.

Это центральный механизм, который позволяет:
1. Регистрировать функции-отчёты под строковым именем (например, "average-gdp").
2. Получать функцию по имени, когда пользователь указывает --report.
3. Получать список всех доступных отчётов (для --help и валидации).

Паттерн "реестр" (или "plugin registry") позволяет добавлять новые отчёты
без изменения существующего кода. Нужно только:
- написать новую функцию с декоратором @register("имя-отчёта"),
- импортировать модуль в reports/__init__.py.

Как это работает «под капотом»:
- _registry — это обычный словарь {имя_отчёта: функция}.
- Декоратор @register("имя") при загрузке модуля вызывается Python'ом,
  записывает функцию в _registry и возвращает её без изменений.
- Когда пользователь запускает скрипт с --report average-gdp,
  вызывается get_report("average-gdp"), которая находит нужную функцию
  в словаре и возвращает её.
"""

# Приватный словарь: ключ — строковое имя отчёта, значение — функция.
# Приватный (с подчёркиванием), потому что внешний код должен
# взаимодействовать через функции register / get_report / get_available_reports,
# а не лезть в словарь напрямую.
_registry: dict[str, Callable] = {}


def register(name: str) -> Callable:
    """
    Декоратор-фабрика для регистрации функции-отчёта.

    Используется так:

        @register("average-gdp")
        def average_gdp(data):
            ...
            return headers, rows

    Что происходит при выполнении этого кода:
    1. Python видит @register("average-gdp") и вызывает register("average-gdp").
    2. register возвращает функцию decorator.
    3. Python вызывает decorator(average_gdp).
    4. Внутри decorator: _registry["average-gdp"] = average_gdp.
    5. decorator возвращает average_gdp без изменений
       (т.е. саму функцию не оборачивает и не меняет).

    Args:
        name: Строковое имя отчёта, которое пользователь будет указывать
              в --report. Например: "average-gdp".

    Returns:
        Функцию-декоратор, которая принимает функцию отчёта
        и записывает её в реестр.
    """

    def decorator(func):
        # Сохраняем функцию в реестр под указанным именем.
        _registry[name] = func
        # Возвращаем функцию как есть — декоратор не модифицирует поведение,
        # а только «регистрирует» функцию.
        return func

    return decorator


def get_report(name: str) -> Callable:
    """
    Получить функцию отчёта по имени.

    Если имя не найдено в реестре — выбрасываем ValueError
    с подсказкой, какие отчёты доступны. Это полезно для отладки:
    пользователь сразу видит, что он мог иметь в виду.

    Args:
        name: Строковое имя отчёта. Например: "average-gdp".

    Returns:
        Функцию-отчёт, которая принимает data (список словарей)
        и возвращает кортеж (headers, rows).

    Raises:
        ValueError: Если отчёт с таким именем не зарегистрирован.
    """
    # эта проверка избыточна так как в ArgumentParser есть параметр choices=get_available_reports() —
    # и argparse проверит, что значение входит в список зарегистрированных отчётов
    if name not in _registry:
        # Формируем строку с доступными отчётами для удобного сообщения об ошибке.
        available = ", ".join(sorted(_registry.keys()))
        raise ValueError(f"Unknown report: '{name}'. Available reports: {available}")
    return _registry[name]


def get_available_reports() -> list[str]:
    """
    Получить отсортированный список имён всех зарегистрированных отчётов.

    Используется в main.py для параметра choices в argparse:
    argparse автоматически проверит, что пользователь ввёл одно
    из допустимых значений, и покажет их в --help.

    Returns:
        Отсортированный список строк. Например: ["average-gdp", "unemployment", ...].
    """
    return sorted(_registry.keys())
