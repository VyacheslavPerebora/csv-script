# Модуль для чтения CSV-файлов с экономическими данными.


import csv
from typing import Dict, Iterable, List


def read_csv_files(file_paths: Iterable[str]) -> List[Dict[str, str]]:
    """
    Читает несколько CSV-файлов и объединяет их содержимое в один список.

    Алгоритм работы:
    1. Создаём пустой список data, куда будем складывать все строки.
    2. Проходим по каждому пути из file_paths.
    3. Открываем файл с кодировкой UTF-8.
       - newline="" нужен по документации модуля csv, чтобы csv.reader
         сам корректно обрабатывал переносы строк внутри полей.
    4. Создаём csv.DictReader, который:
       - читает первую строку как заголовки (country, year, gdp, ...),
       - каждую следующую строку превращает в словарь вида
         {"country": "United States", "year": "2023", "gdp": "25462", ...}.
       - Все значения остаются строками — преобразование типов
         происходит уже в конкретном отчёте.
    5. Через extend добавляем все строки текущего файла в общий список.
    6. Если файл не найден, Python выбросит FileNotFoundError —
       мы его не перехватываем здесь, а пробрасываем наверх,
       чтобы вызывающий код (main.py) решил, как реагировать.

    Args:
        file_paths: Итерируемый объект (список, кортеж и т.д.) с путями
                    к CSV-файлам. Например: ["data1.csv", "data2.csv"].

    Returns:
        Список словарей. Каждый словарь — одна строка из CSV.
        Пример одного элемента:
        {
            "country": "United States",
            "year": "2023",
            "gdp": "25462",
            "gdp_growth": "2.1",
            ...
        }

    Raises:
        FileNotFoundError: Если какой-либо из файлов не существует.
    """
    # Общий список, куда попадут строки из ВСЕХ файлов.
    # Это позволяет формировать единый отчёт по нескольким файлам.
    data = []

    for file_path in file_paths:
        with open(file_path, newline="", encoding="utf-8") as csvfile:
            reader = csv.DictReader(csvfile)

            data.extend(reader)

    return data
